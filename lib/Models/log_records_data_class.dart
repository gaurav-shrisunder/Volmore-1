 class Record {
  final String group;
  final String title;
  final String host;
  final String address;
  final String timeElapsed;
  final String signature;
  final String location;
  final String timer;

  Record({
    required this.group,
    required this.title,
    required this.host,
    required this.address,
    required this.timeElapsed,
    required this.signature,
    required this.location,
    required this.timer,
  });
}


